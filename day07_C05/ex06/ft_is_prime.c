int ft_is_prime(int nb)
{
	long int i[3];

	i[0] = 2;
	if (nb < 2)
		return (0);
	while (i[0] <= nb)
	{
		i[1]= 2;
		while (i[1] <= nb)
		{
			if ((i[0] * i[1]) == nb)
				return (0);
			if ((i[0] * i[1]) > nb)
				break;
			i[1]++;
		}
		i[0]++;
	}
	return (1);
}
