#include "../includes/ft.h"

int	division(int num_1, int num_2)
{
	return (num_1 / num_2);
}

int	addition(int num_1, int num_2)
{
	return (num_1 + num_2);
}

int	modulo(int num_1, int num_2)
{
	return (num_1 % num_2);
}

int	multiplication(int num_1, int num_2)
{
	return (num_1 * num_2);
}

int	substraction(int num_1, int num_2)
{
	return (num_1 - num_2);
}
