#include <unistd.h>

void	ft_putnbr(int nb)
{
	char c;
	long nb22;

	nb22 = nb;
	if (nb22 == 0)
		write(1, &"0", 1);
	if (nb22 < 0)
	{
		nb22 = nb22 * -1;
		write(1, &"-", 1);
	}
	if (nb22 >= 10)
	{
		ft_putnbr(nb22 / 10);
		ft_putnbr(nb22 % 10);
	}
	else if (nb22 != 0)
	{
		c = nb22 + 48;
		write(1, &c, 1);
	}
}
